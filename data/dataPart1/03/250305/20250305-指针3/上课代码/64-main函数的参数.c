#include <stdio.h>
#include <stdbool.h>
#include <string.h>
#include <stdlib.h>
 
int main(int argc, char *argv[]) {
	
	printf("argc的值：%d \n", argc);
	
	for(int i = 0 ; i < argc; i++){
		printf("%s \n", argv[i]);
	}
	
	
	
    return 0;
}























